    private void tblComponentTypeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblComponentTypeMouseClicked
        // TODO add your handling code here:
        try {
            int row = tblComponentType.getSelectedRow();
            
            if (row >= 0) {
                String maSo = (String) tblComponentType.getValueAt(row, 0);
                LoaiLinhKienDao dao = new LoaiLinhKienDao();
                LoaiLinhKien llk = dao.findByMaLoai(maSo);
                
                if (llk!=null) {
                    txtMaLoai.setText(llk.getMaLoai());
                    txtTenLoai.setText(llk.getTenLoai());
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblComponentTypeMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaLoai, sb, "Cần nhập Mã loại linh kiện");
        DataValidator.validateEmpty(txtTenLoai, sb, "Cần nhập Tên loại linh kiện");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        try {
            LoaiLinhKien llk = new LoaiLinhKien();
            llk.setMaLoai(txtMaLoai.getText());
            llk.setTenLoai(txtTenLoai.getText());

            LoaiLinhKienDao dao = new LoaiLinhKienDao();
            if (dao.insert(llk)) {
                MessageDialogHelper.showMessageDialog(this, "Loại linh kiện đã được lưu", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showMessageDialog(this, "Không thể lưu loại linh kiện", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaLoai, sb, "Cần nhập Mã loại linh kiện");
        DataValidator.validateEmpty(txtTenLoai, sb, "Cần nhập Tên loại linh kiện");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(this,
            "Bạn có muốn cập nhật loại linh kiện không?", "Hỏi") == JOptionPane.NO_OPTION) {
        return;
        }
        try {
            LoaiLinhKien llk = new LoaiLinhKien();
            llk.setMaLoai(txtMaLoai.getText());
            llk.setTenLoai(txtTenLoai.getText());

            LoaiLinhKienDao dao = new LoaiLinhKienDao();
            if (dao.update(llk)) {
                MessageDialogHelper.showMessageDialog(this, "Loại linh kiện đã được cập nhật", "Thông báo");
                loadDataToTable();
            } else {
                MessageDialogHelper.showMessageDialog(this, "Không thể cập nhật loại linh kiện", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaLoai, sb, "Cần nhập Mã loại linh kiện");
        if (sb.length() > 0) {
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Lỗi");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(this,
            "Bạn có muốn xóa dữ liệu về loại linh kiện này không?", "Hỏi") == JOptionPane.NO_OPTION) {
        return;
        }
        try {
            LoaiLinhKienDao dao= new LoaiLinhKienDao();
            if (dao.delete(txtMaLoai.getText())) {
                MessageDialogHelper.showMessageDialog(this, "Dữ liệu về loại linh kiện đã được xóa", "Thông báo");
                loadDataToTable();
                btnNewActionPerformed(evt);
            } else {
                MessageDialogHelper.showMessageDialog(this, "Không thể xóa dữ liệu về loại linh kiện này", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

