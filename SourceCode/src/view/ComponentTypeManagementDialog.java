    private void initTable() {
        tblModel = new DefaultTableModel(
            new Object[][] {
            },
            new String[]{"Mã loại", "Tên loại"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
               //all cells false
               return false;
            }
            
            Class[] columnTypes = new Class[] {
                Object.class, Object.class
            };
            public Class getColumnClass (int columnIndex) {
                return columnTypes[columnIndex];
            }
        };
        tblComponentType.setModel(tblModel);
    }
    
    private void loadDataToTable() {
        try {
            LoaiLinhKienDao dao = new LoaiLinhKienDao();
            ArrayList<LoaiLinhKien> list = dao.findAll();
            tblModel.setRowCount(0);
            for (LoaiLinhKien it : list) {
                tblModel.addRow(new Object[] {
                    it.getMaLoai(), it.getTenLoai()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }

